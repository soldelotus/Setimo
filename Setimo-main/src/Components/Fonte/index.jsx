import * as React from 'react';
import Button from '@mui/material/Button';
import Menu from '@mui/material/Menu';
import MenuItem from '@mui/material/MenuItem';
import Fade from '@mui/material/Fade';
import { useUserPreferences } from '../../hooks/useUserPreferences';

export default function FadeMenu() {

    const {changeBaseFontSize} = useUserPreferences()

  const [anchorEl, setAnchorEl] = React.useState(null);
  const open = Boolean(anchorEl);
  const handleClick = (event) => {
    setAnchorEl(event.currentTarget);
  };
  const handleClose = () => {
    setAnchorEl(null);
  };

  return (
    <div>
      <Button
        id="fade-button"
        aria-controls={open ? 'fade-menu' : undefined}
        aria-haspopup="true"
        aria-expanded={open ? 'true' : undefined}
        onClick={handleClick}
      >
        <img src="/fonte.png" alt="" />
      </Button>
      <Menu
        id="fade-menu"
        MenuListProps={{
          'aria-labelledby': 'fade-button',
        }}
        anchorEl={anchorEl}
        open={open}
        onClose={handleClose}
        TransitionComponent={Fade}
      >
        <MenuItem onClick={() => changeBaseFontSize('14px')}>A-</MenuItem>
        <MenuItem onClick={() => changeBaseFontSize('16px')}>A</MenuItem>
        <MenuItem onClick={() => changeBaseFontSize('18px')}>A+</MenuItem>
      </Menu>
    </div>
  );
}